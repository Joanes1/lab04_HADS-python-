class Mochila:
    
    def __init__():
        self.objetos = []
    
    def __str__(self):
        for objeto in self.objetos:
            print(objeto)

    def introducir_objeto(a):
        self.objetos.append(a)
    
    def main():
        m2 = Mochila()
        m3 = Mochila()
        m2.introducir_objeto('bocadillo')
        m2.introducir_objeto('juego de llaves')
        print(m2)
        print(m3)

    if __name__== "__main__":
        main()
